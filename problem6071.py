n = 1                 # 변수 n을 1로 초기화합니다.
while n != 0:         # n이 0이 아닌 동안 반복합니다.
    n = int(input())  # 사용자로부터 정수를 입력받고, 그 값을 n에 할당합니다.
    if n != 0:        # 만약 n이 0이 아니라면 아래의 코드를 실행합니다.
        print(n)      # 입력된 값 n을 출력합니다.
